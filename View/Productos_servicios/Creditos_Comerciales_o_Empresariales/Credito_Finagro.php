      <h3>¿Qué es un Credito Finagro?</h3>
<p>Un crédito Finagro es un préstamo diseñado para financiar proyectos agrícolas, a diferencia de un crédito de consumo que cubre gastos personales. Su objetivo principal es impulsar la capacidad de una empresa agrícola para generar ingresos y crecer.</p>    
    </div>

    <div class="info-box">
      <center>  <img src="../../../Assets/Img/Creditos/A3.png" alt="" ></center>
      <h3>Beneficios</h3>
      <ul>
<li>Tasas de Interés Bajas: Son más económicas que los préstamos normales, y hay líneas especiales con subsidios adicionales.</li>
<li>Pagos Flexibles: Los plazos y periodos de gracia se ajustan a los ciclos de tu producción (cuando cosechas o vendes).</li>
<li>Cubre Muchos Proyectos: Financian desde la siembra y el ganado hasta la maquinaria y la transformación de tus productos.</li>
<li>Incentivos Económicos: Puedes recibir reembolsos directos por invertir en tu finca (Incentivo a la Capitalización Rural - ICR) y subsidios para seguros.</li>
<li>Facilita Garantías: Ayuda a respaldar tu crédito si no tienes suficientes garantías propias.</li>
<li>Fomenta la Sostenibilidad: Apoya proyectos que cuidan el medio ambiente y promueven la inclusión social.</li>
      </ul>

    </div>


    <div class="info-box">
