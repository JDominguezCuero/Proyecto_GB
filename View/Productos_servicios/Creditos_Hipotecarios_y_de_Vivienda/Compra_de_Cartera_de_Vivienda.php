      <h3>¿Qué es una Compra de Cartera de Vivienda
      </h3>
<p>La compra de cartera de vivienda permite trasladar un crédito existente hipotecario o leasing habitacional a otra entidad financiera con mejores condiciones, como tasas más bajas, plazos flexibles y beneficios tributarios, con el objetivo de reducir las cuotas mensuales y optimizar la gestión financiera sin asumir gastos adicionales importantes.
</p>
    </div>

    <div class="info-box">
      <center>  <img src="../../../Assets/Img/Creditos/A3.png" alt="" ></center>
      <h3>Beneficios</h3>
      <ul>
        <li>Reducción de tasas de interés, lo que disminuye el valor total del crédito.</li>
        <li>Plazos más flexibles, adaptados a tu capacidad de pago.</li>
        <li>Cuotas mensuales más bajas, mejorando tu flujo de caja.</li>
        <li>Trámite simplificado, sin necesidad de nueva hipoteca en muchos casos.</li>
        <li>Seguros incluidos, como vida, desempleo o daños al inmueble.</li>
        <li>Beneficios tributarios, al permitir deducir intereses en la declaración de renta.</li>
        <li>Acceso a productos adicionales, como tarjetas de crédito o programas de puntos.</li>
    </div>


    <div class="info-box">
        <center><img src="../../../Assets/Img/Creditos/A2.png" alt=""></center>
      <h3>Requisitos</h3>
      <ul>
        <li>Cédula de ciudadanía o extranjería.</li>
        <li>Solicitud de crédito diligenciada.</li>
        <li>Carta de saldo de la entidad financiera donde está el crédito hipotecario.</li> 
<li>Certificado de tradición y libertad del inmueble (vigencia no mayor a 30 días).</li>
<li>Copia de la escritura pública del inmueble.</li>
<li>Avalúo del bien (si el banco lo solicita).</li>
<li>Certificación laboral o ingresos.</li>
<li>Extractos bancarios (últimos 3 meses).</li>
<li>Paz y salvo del crédito, si ya ha sido cancelado por otra entidad.</li>

    </ul>
