      <h3>¿Qué es una compra de cartera de crédito de educación?</h3>
      <p>Es un producto financiero que te permite trasladar tu crédito educativo actual desde otra entidad hacia un nuevo banco o cooperativa. El objetivo es obtener mejores condiciones como una tasa de interés más baja, una menor cuota mensual o plazos más amplios para facilitar el pago.</p>
    </div>

    <div class="info-box">
      <center>  <img src="../../../Assets/Img/Creditos/A3.png" alt="" ></center>
      <h3>Beneficios</h3>
      <ul>
        <li>Reducción de la tasa de interés frente al crédito actual.</li>
        <li>Disminución de la cuota mensual.</li>
        <li>Unificación con otros créditos educativos (si aplica).</li>
        <li>Plazos más cómodos para pagar.</li>
        <li>Mejores condiciones según tu perfil financiero.</li>
        <li>No siempre se requiere codeudor.</li>
        <li>Opción de incluir otros gastos relacionados con la educación.</li>
       </ul>
</div>






    <div class="info-box">
        <center><img src="../../../Assets/Img/Creditos/A2.png" alt=""></center>
      <h3>Requerimientos</h3>
      <ul>
      <li>Documento de identidad.</li>
      <li>Solicitud de crédito diligenciada.</li>
      <li>Carta de saldo del crédito educativo.</li>
      <li>Extractos de pagos recientes del crédito educativo.</li>
      <li>Certificado de matrícula (si el crédito sigue activo y el estudiante aún está en formación).</li>
      <li>Certificación de ingresos del codeudor o del responsable de pago.</li>
      <li>Extractos bancarios del titular o codeudor.</li>
      </ul>
