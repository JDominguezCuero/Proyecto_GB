      <h3>¿Qué es una Compra de cartera de microcrédito?</h3>
      <p>Es un producto financiero que permite trasladar microcréditos activos (pequeños préstamos otorgados generalmente para actividades productivas o emprendimientos) desde una entidad financiera actual hacia otra.</p>
