      <h3>Requerimientos</h3>
      <ul>
        <li>Documento de identidad (original y copia).</li>
    <li>Solicitud de crédito diligenciada.</li>
    <li>Carta de saldo del microcrédito vigente (saldo total, entidad, condiciones).</li>
    <li>Certificado de ingresos (puede ser informal, validado por asesor o firmada por contador).</li>
    <li>Certificación de actividad económica (puede ser una declaración juramentada).</li>
    <li>Extractos bancarios o registros de movimientos (si aplica).</li>
    <li>Referencias personales o comerciales.</li>
